class Solution{
    public:
    struct Node* modifyTheList(struct Node *head)
    {
        //add code here.
        Node* temp = head;
        vector<int> nodes;
        
        while(temp) 
        {
            nodes.push_back(temp->data);
            temp = temp->next;
        }
        
        int n = nodes.size();
        int cnt = 0;
        
        temp = head;
        while(temp)
        {
            if(cnt < n / 2)
                temp->data = nodes[n - 1 - cnt] - nodes[cnt];
            else
                temp->data = nodes[n - 1 - cnt];
            temp = temp->next;
            cnt++;
        }
        
        return head;
    }
};

