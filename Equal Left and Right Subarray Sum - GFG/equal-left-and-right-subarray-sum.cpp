class Solution {
  public:
    int equalSum(int n, vector<int> &arr) {
        // code here
        if(n == 1) return 1;
        
        long long sum = 0;
        for(int i = 0; i < n; i++)
            sum += arr[i];
            
        long long currSum = arr[0];
        
        for(int i = 1; i < n - 1; i++)
        {
            if(currSum == sum - currSum - arr[i])
                return i + 1;
            currSum += arr[i];
        }
        
        return -1;
    }
};


